class Calculator:

    def add(self, a, b):
        return round(a + b, 2)

    def add1(self, a, b):
        return round(a + b, 2)

    def div(self, a, b):
        return a / b

    def plus(self, a, b):
        return round(a * b, 2)

    def minus(self, a, b):
        return a - b
